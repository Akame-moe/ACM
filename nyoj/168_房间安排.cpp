#include<stdio.h>
#include<string.h>
int main()
{
    int n,room,di,jiu;
    int f[191];
    int T;
    int i;
    int max;
    scanf("%d",&T);
    while(T--)
    {
        scanf("%d",&n);
        memset(f,0,sizeof(f));
        while(n--)
        {
            scanf("%d%d%d",&room,&di,&jiu);
            for(i=di;i<di+jiu;i++)
            {
                f[i]+=room;
            }
        }
        max=0;
        for(i=0;i<191;i++)
            if(f[i]>max)
                max=f[i];
        printf("%d\n",max);
    }
    return 0;


}        